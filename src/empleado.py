class Empleado:
    def __init__(self, nombre, apellido_paterno, apellido_materno, fecha_de_nacimiento, direccion, sexo, grado_estudios, horario_inicio,
                horario_fin, historial):
        self.nombre = nombre
        self.apellido_paterno = apellido_paterno
        self.apellido_materno = apellido_materno 
        self.fecha_de_nacimiento = fecha_de_nacimiento
        self.direccion = direccion
        self.sexo = sexo
        self.grado_estudios = grado_estudios
        self.horario_inicio = horario_inicio
        self.horario_fin = horario_fin
        self.historial = historial

